def svm_loss(x, y):
    """
    Computes the loss and gradient using for multiclass SVM classification.

    Inputs:
    - x: Input data, of shape (N, C) where x[i, j] is the score for the jth
      class for the ith input.
    - y: Vector of labels, of shape (N,) where y[i] is the label for x[i] and
      0 <= y[i] < C

    Returns a tuple of:
    - loss: Scalar giving the loss
    - dx: Gradient of the loss with respect to x
    """
    loss, dx = None, None
    ###########################################################################
    # TODO: Implement loss and gradient for multiclass SVM classification.    #
    # This will be similar to the svm loss vectorized implementation in       #
    # cs231n/classifiers/linear_svm.py.                                       #
    ###########################################################################
    # *****START OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****
    N = x.shape[0]
    correct = x[np.arange(N), y]
    margins = np.maximum(0, x - correct[:, np.newaxis] + 1.0)
    margins[np.arange(N), y] = 0
    loss = np.sum(margins) / N
    num_pos = np.sum(margins > 0, axis=1)
    dx = np.zeros_like(x)
    dx[margins > 0] = 1
    dx[np.arange(N), y] -= num_pos
    dx /= N    
    # *****END OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****
    ###########################################################################
    #                             END OF YOUR CODE                            #
    ###########################################################################
    return loss, dx


def softmax_loss(x, y):
    """
    Computes the loss and gradient for softmax classification.

    Inputs:
    - x: Input data, of shape (N, C) where x[i, j] is the score for the jth
      class for the ith input.
    - y: Vector of labels, of shape (N,) where y[i] is the label for x[i] and
      0 <= y[i] < C

    Returns a tuple of:
    - loss: Scalar giving the loss
    - dx: Gradient of the loss with respect to x
    """
    loss, dx = None, None
    ###########################################################################
    # TODO: Implement the loss and gradient for softmax classification. This  #
    # will be similar to the softmax loss vectorized implementation in        #
    # cs231n/classifiers/softmax.py.                                          #
    ###########################################################################
    # *****START OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****
    
    # A의 원소 a에 대하여, exp(a)/sum(exp(all a[i]) = exp(a + c)/sum(exp(a[i] + c)) 이다.
    # 즉, softmax는 어느 원소에 어떤 수를 더하는 형태로 exp를 취하더라도 값이 변하지 않는다.
    # softmax는 e의 지수를 포함하므로, exponential하게 큰 폭으로 증가한다. 이는 overflow문제를 야기할 수 있다.
    # 따라서 위 성질을 활용해, a에 대해 exp를 취하는 것이 아니라,a - c에 대해서 exp를 취하며, c는 a 중 최댓값이다.
    # 예를 들어, a = [1010, 1000, 990] 이라면 a - c = [0, -10, -20] 이다.
    probs = np.exp(x - np.max(x, axis=1, keepdims=True))
    probs /= np.sum(probs, axis=1, keepdims=True)
    N = x.shape[0]
    loss = - np.sum(np.log(probs[np.arange(N), y])) / N
    dx = probs.copy()
    # https://ratsgo.github.io/deep%20learning/2017/10/02/softmax/
    dx[np.arange(N), y] -= 1 # Softmax Loss의 x에 대한 gradient는 입력벡터에 소프트맥스를 취한 후 , 정답 레이블에 해당하는 요소만 -1 을 취한다. 
    dx /= N
    # *****END OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****
    ###########################################################################
    #                             END OF YOUR CODE                            #
    ###########################################################################
    return loss, dx
